			<dt>Prima generazione - PC-98</dt>
			<dd><p>Il primo gioco di Touhou, <strong>Highly Responsive to Prayers</strong> (靈異伝), distribuito nel 1997, è stato realizzato unicamente per NEC <em>PC-98</em>, programmato per Intel 8086 ed era a 16bit. Questo gioco è molto diverso dai Touhou attuali, lo scopo era quello di sparare a una pallina su schermo per muoverla verso dei blocchetti, in modo da distruggerli, simile ad Arkanoid del 1976.<br/>
Il titolo è stato sviluppato da <em>ZUN soft</em>, successivamente rinominata in <strong>Team Shanghai Alice</strong>, o Shanhai Arisu Gengakudan, composta unicamente da un unico membro: ZUN, nome reale Jun'ya Ota (太田順也).<br/>
<strong>ZUN</strong> si occupa di qualsiasi compito nella produzione dei titoli, dalla programmazione alla realizzazione delle musiche, molto apprezzare, a differenza dei disegni, che sono spesso criticati per essere più simili a bozze.<br/>
Con i successivi giochi si iniziarono a delimitarsi pian piano gli aspetti che caratterizzano il brand, già iniziando dal secondo gioco, <em>Story of Eastern Wonderland</em> (封魔録) si può notare una maggiore aderenza alla categoria Bullet Hell.<br/>
</p></dd>
			<dt>Seconda generazione - Windows e Comitek</dt>
<dd><p>Nel 2002 uscì il primo gioco ad essere compatibile per Windows, <strong>the Embodiment of Scarlet Devil</strong> (紅魔郷), sesto titolo del <!-- FRANCESE -->franchise, che rappresentò quindi una nuova generazione per la serie, definita come "prima generazione Windows".<br/>
Questo titolo riscosse un enorme successo, ancora oggi rappresenta l'intera serie ed i personaggi presentati sono i più diffusi su internet, con centinaia e centinaia di opere create dai fan a riguardo.<br/>
La diffusione dei lavori derivati è data anche dal fatto che secondo la licenza del gioco unicamente i materiali del gioco sono protetti, mentre i personaggi, i design e la trama sono liberamente utilizzabili da chiunque, anche per scopi commerciali, questo ha portato a una enorme produzione di lavori indipendenti, molti dei quali venduti al <strong>Comitek</strong>, o <em>Comic Market</em>: consiste in una manifestazione che si svolge due volte l'anno, durante la quale si possono vendere gadget, fumetti o qualsiasi altro prodotto sviluppato indipendentemente a un vasto pubblico, anche i nuovi capitoli di Touhou vengono presentati e venduti durante queste manifestazioni.<br/>
I giochi successivi apportarono molti miglioramenti grafici al gioco, mantenendo una qualità in linea con il sesto capitolo.
</p></dd>
